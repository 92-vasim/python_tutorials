x = 20
y = 10

# arithmetic operators
print(f"Addition: {x+y}")
print(f"Subtraction: {x-y}")
print(f"Multiplication: {x*y}")
print(f"Division: {x/y}")

# floor
print(f"Floor division: {x//y}")

# square
print(f"Squaring: {x**x}")

# modulus
print(f"Modulus: {x%y}")

